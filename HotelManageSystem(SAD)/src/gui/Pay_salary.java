    private void Loadpayships() {
        try {

            ResultSet resultSet = Mysql2.msearch("SELECT * FROM paysalary INNER JOIN employee ON paysalary.employee_id=employee.id INNER JOIN emp_salary ON emp_salary.employee_id=employee.id");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();

                vector.add(resultSet.getString("emp_id"));
                vector.add(resultSet.getString("Salary"));
                vector.add(resultSet.getString("month"));
                vector.add(resultSet.getString("fname"));
                vector.add(resultSet.getString("date"));
                vector.add(resultSet.getString("status"));

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    


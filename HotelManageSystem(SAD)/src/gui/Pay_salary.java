    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String employee = String.valueOf(jComboBox1.getSelectedItem());
        String month = String.valueOf(jComboBox2.getSelectedItem());
        String salary = jLabel6.getText();

        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String formattedNow = now.format(formatter);

        if (employee.equals("Choose Employee Name")) {
            JOptionPane.showMessageDialog(this, "Select Employee", "Wanning", JOptionPane.WARNING_MESSAGE);
        } else if (month.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select Month", "Wanning", JOptionPane.WARNING_MESSAGE);
        } else if (salary.equals("0")) {
            JOptionPane.showMessageDialog(this, "Salary Not Assign!", "Wanning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Mysql2.miud("INSERT INTO paysalary (`date`,`status`,`employee_id`,`month`) "
                        + "VALUES('"+formattedNow+"','Pay','"+EMPMap.get(String.valueOf(employee))+"','"+month+"')");
                 JOptionPane.showMessageDialog(this, "Payment Successfull", "sucess", JOptionPane.INFORMATION_MESSAGE);
                 this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        String employee = String.valueOf(jComboBox1.getSelectedItem());

        try {
            ResultSet resultSet = Mysql2.msearch("SELECT * FROM emp_salary WHERE emp_salary.employee_id='" + EMPMap.get(String.valueOf(employee)) + "'");
            if (resultSet.next()) {
                String salary = String.valueOf(resultSet.getString("Salary"));
                jLabel6.setText(salary);

            } else {
                JOptionPane.showMessageDialog(this, "Salary Not Assign Employee", "Wanning", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String itname = jTextField1.getText();
       String itshortname = jTextField2.getText();
       
        if (itname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Unit Nmae ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (itshortname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Unit Short Name ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                Mysql2.miud("INSERT INTO unit_items (`Items_Name`,`Short_Name`) VALUES('"+itname+"','"+itshortname+"');");
                JOptionPane.showMessageDialog(this, "Added Unit", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


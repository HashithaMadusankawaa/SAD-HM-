     private void Loadguest_details() {
        try {
            ResultSet resultSet = Mysql2.msearch("SELECT * FROM guest_details INNER JOIN gender ON guest_details.gender_id=gender.id ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("fname"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("date_of_Birth"));
                vector.add(resultSet.getString("gender_name"));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String empid = String.valueOf(jComboBox1.getSelectedItem());
        String status = String.valueOf(jComboBox2.getSelectedItem());
        String salary = jTextField3.getText();

        if (empid.equals("Choose Employee Name")) {
            JOptionPane.showMessageDialog(this, "Please Select Employee", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (salary.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Salary", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!salary.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Invalid Salary", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
               
                ResultSet resultSet = Mysql2.msearch("SELECT * FROM emp_salary WHERE employee_id='"+EMPMap.get(String.valueOf(empid))+"'");
                if (resultSet.next()) {
                     JOptionPane.showMessageDialog(this, "Already Salary Add.", "Warning", JOptionPane.WARNING_MESSAGE);
                }else{
                     Mysql2.miud("INSERT INTO emp_salary (`Salary`,`emp_Status_id`,`employee_id`)"
                        + " VALUES ('"+salary+"','"+statusMap.get(String.valueOf(status))+"','"+EMPMap.get(String.valueOf(empid))+"')");
                 JOptionPane.showMessageDialog(this, "Succesfull Salary Add.", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                 this.dispose();
                }
               
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        String empname = String.valueOf(jComboBox1.getSelectedItem());
        if (!empname.equals("Choose Employee Name")) {
            try {
                ResultSet resultSet = Mysql2.msearch("SELECT * FROM employee INNER JOIN designation ON employee.designation_id=designation.id WHERE employee.fname='" + empname + "'");
                if (resultSet.next()) {
                    String JOB = resultSet.getString("designation_name");
                    jTextField2.setText(JOB);
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged


    private void LoadRoomFacilitiesDetails() {
        try {
            ResultSet resultSet = Mysql2.msearch("SELECT * FROM items_list INNER JOIN items_category ON items_list.Items_Category_id=items_category.id INNER JOIN unit_items ON items_list.Unit_Items_id=unit_items.id ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("Items_Name"));
                vector.add(resultSet.getString("Category_Name"));
                vector.add(resultSet.getString("Short_Name"));
                vector.add(resultSet.getString("Total_Item"));
                vector.add(resultSet.getString("Item_Used"));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


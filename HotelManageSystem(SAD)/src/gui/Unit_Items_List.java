     private void LoadUnitList(){
       try {
     
            ResultSet resultSet = Mysql2.msearch("SELECT * FROM unit_items ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                
                vector.add(resultSet.getString("id")); 
                vector.add(resultSet.getString("Items_Name"));
                vector.add(resultSet.getString("Short_Name"));
               
     
                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
   }

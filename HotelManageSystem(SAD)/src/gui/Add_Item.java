   private void LoadCategoryName() {
        try {

            ResultSet resultSet = Mysql2.msearch("SELECT * FROM `items_category`");
            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("Category_Name"));
                categorynameMap.put(resultSet.getString("Category_Name"), resultSet.getString("id"));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

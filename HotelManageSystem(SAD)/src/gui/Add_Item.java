    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String unitName = String.valueOf(jComboBox1.getSelectedItem());
        String categoryName = String.valueOf(jComboBox2.getSelectedItem());
        String ItemName = jTextField3.getText();
        String totalItem = jTextField4.getText();
        String usedItem = jTextField5.getText();
        
        if (unitName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Unit Name ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (categoryName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Catgory Name ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (ItemName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Item Name ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (totalItem.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Item Total ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (usedItem.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Used Item ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                Mysql2.miud("INSERT INTO items_list (`Items_Name`,`Total_Item`,`Item_Used`,`Unit_Items_id`,`Items_Category_id`) "
                        + "VALUES('"+ItemName+"','"+totalItem+"','"+usedItem+"','"+unitnameMap.get(String.valueOf(unitName))+"','"+categorynameMap.get(String.valueOf(categoryName))+"')");
                            JOptionPane.showMessageDialog(this, "Sucessfull ", "sucess", JOptionPane.INFORMATION_MESSAGE);
                            this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


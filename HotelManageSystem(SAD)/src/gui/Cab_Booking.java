   private void LoadCabBookingList(){
       try {
     
            ResultSet resultSet = Mysql2.msearch("SELECT * FROM `car_booking` ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                
                vector.add(resultSet.getString("id")); 
                vector.add(resultSet.getString("datetime"));
                vector.add(resultSet.getString("car_booking_type_id"));
                vector.add(resultSet.getString("cab_list_id"));
                vector.add(resultSet.getString("employee_id"));
                vector.add(resultSet.getString("guest_details_id"));
     
                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
   }
